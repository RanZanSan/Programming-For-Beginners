using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _18.Choosing_A_Variable_Name
{
    class Program
    {
        static void Main(string[] args)
        {
            // Представим себе, что программа из прошлого урока выглядит так:
            var x = "Father!";
            Console.WriteLine(x);
            Console.WriteLine(x);
            Console.ReadKey();

            /*
             * Она по прежнему работает, но в ней изменилось имя переменной на x.
             * Компьютеру без разницы, как мы называем переменные, это бездушная машина, но вот программистам — нет.
             * Мы гораздо чаще читаем код, чем пишем.
             * Причём не свой, а написанный другими людьми.
             * От качества и понятности имён переменных зависит половина успеха в анализе кода.
             */
        }
    }
}